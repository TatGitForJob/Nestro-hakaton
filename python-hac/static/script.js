const init = () => {
    var myPlacemark
    const map = new ymaps.Map('mapContainer', {
        center: [43.88400017926909,17.623901181640566],
        zoom: 7,
    });
    map.events.add('click', function (e) {
        var coords = e.get('coords');

        // Если метка уже создана – просто передвигаем ее.
        if (myPlacemark) {
            myPlacemark.geometry.setCoordinates(coords);
            myPlacemark.name=coords
        }
        // Если нет – создаем.
        else {
            myPlacemark = createPlacemark(coords);
            map.geoObjects.add(myPlacemark);
            // Слушаем событие окончания перетаскивания на метке.
            myPlacemark.events.add('dragend', function () {
                getAddress(myPlacemark.geometry.getCoordinates());
            });
        }
        getAddress(coords)
    });

    // Создание метки.
    function createPlacemark(coords) {
        return new ymaps.Placemark(coords, {
            iconCaption: 'поиск...'
        }, {
            preset: 'islands#violetDotIconWithCaption',
            draggable: true,
            name: coords
        });
    }

    function getAddress(coords) {
        myPlacemark.properties.set('iconCaption', 'поиск...');
        ymaps.geocode(coords).then(function (res) {
            var firstGeoObject = res.geoObjects.get(0),
                address = firstGeoObject.getAddressLine();

            myPlacemark.properties
                .set({
                    // Формируем строку с данными об объекте.
                    iconCaption: [
                        // Название населенного пункта или вышестоящее административно-территориальное образование.
                        firstGeoObject.getLocalities().length ? firstGeoObject.getLocalities() : firstGeoObject.getAdministrativeAreas(),
                        // Получаем путь до топонима, если метод вернул null, запрашиваем наименование здания.
                        firstGeoObject.getThoroughfare() || firstGeoObject.getPremise()
                    ].filter(Boolean).join(', '),
                    // В качестве контента балуна задаем строку с адресом объекта.
                    balloonContentHeader: address,
                    balloonContentBody: coords
                });
            myInput.value = address;
        });
    }



    let activeCategory = "category1";

    const showCategory = (category) => {

        categoriesData[category].forEach((item) => {
            var placemark
            if (category==="category2") {
                placemark = new ymaps.Placemark([item.lat, item.lon], {
                        hintContent: item.name,
                        balloonContent: item.name,
                        balloonContentBody: item.name,
                    },
                    {
                        iconLayout: 'default#image',
                        // Своё изображение иконки метки.
                        // Размеры метки.
                        iconImageSize: [20, 25],
                        iconImageHref: 'static/nestro.jpg'
                    },
                );
            }else{
                placemark = new ymaps.Placemark([item.lat, item.lon], {
                        hintContent: item.name,
                        balloonContent: item.name,
                        balloonContentBody: item.name,
                    },
                );
            }
            map.geoObjects.add(placemark);
        });

        activeCategory = category;
    }

    const categoryButtons = document.querySelectorAll('.category-button');
    categoryButtons.forEach((button) => {
        button.addEventListener('click', (e) => {
            const category = e.currentTarget.dataset.category;
            if (category==="category1") {
                map.geoObjects.removeAll();
                showCategory("category2");
                showCategory("category3");
            }else{
                map.geoObjects.removeAll();
                showCategory(category)
            }
        });
    });

    showCategory(activeCategory);
};
